type IconsetName = $Keys<typeof ICONS>;
type IconName = $Keys<typeof ICONS.ionicon>;

type Props = {
    set?: IconsetName, // defaults to "ionicon"
    name: IconName,
    children?: void,
    style?: Style
    // ...textProps
}

class Icon extends Component<Props> {
    render() {
        const { set='ionicon', name, style, ...textProps } = this.props;

        const icons = ICONS[set];
        if (!icons) throw new Error(`Invalid icon set name: "${set}"`);

        const unicode = ICONS[set][name];
        if (!unicode) throw new Error(`Invalid icon name: "${name}"`);

        delete textProps.children; // devuser cannot submit children as props

        return <Text style={style ? [style, styles[set]] : styles[set]} {...textProps}>{unicode}</Text>
    }
}

